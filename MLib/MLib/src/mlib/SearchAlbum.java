    public ArrayList<Song> songList()
    {
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/mlib","sarthak","123");
            String aname=jTextField1.getText();
             String query1="Select song_name,artist_name,album_name,Year,song_id from artist natural join song natural join album where album_name like     "+"'"+aname+"'";
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(query1);
            Song song;
            while(rs.next())
            {
                 song=new Song(rs.getString("song_name"),rs.getString("artist_name"),rs.getString("album_name"),rs.getString("Year"),rs.getInt("song_id"));
                songsList.add(song);
                System.out.println(song.getName());
            }
            con.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
        return songsList;
    }
